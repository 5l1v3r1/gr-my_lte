    dci_gen::sptr
    dci_gen::make(string dci_t,
                  short rnti,
                  bool carrier_ind,
                  uint32 rv_idx,
                  uint32 N_dl_prb,
                  uint32 N_layers,
                  uint32 tx_mode,
                  uint32 N_dl_harq,
                  int mcs,
                  bool ndi,
                  int N_ant,
                  int alloc_len,
                  int RB_start)
    {
        return gnuradio::get_initial_sptr
                (new dci_gen_impl(dci_t,
                                  rnti,
                                  carrier_ind,
                                  rv_idx,
                                  N_dl_prb,
                                  N_layers,
                                  tx_mode,
                                  N_dl_harq,
                                  mcs,
                                  ndi,
                                  N_ant,
                                  alloc_len,
                                  RB_start));
    }

    dci_gen_impl::dci_gen_impl(string dci_t,
                               short rnti,
                               bool carrier_ind,
                               uint32 rv_idx,
                               uint32 N_dl_prb,
                               uint32 N_layers,
                               uint32 tx_mode,
                               uint32 N_dl_harq,
                               int mcs,
                               bool ndi,
                               int N_ant,
                               int alloc_len,
                               int RB_start)
      : gr::sync_block("dci_gen",
              gr::io_signature::make(0, 0, 0),
              gr::io_signature::make(1, 1, sizeof(uint8))),
        dci_type(dci_t)       // this is not yet used!
    {
        // initializing allocation parameters
        alloc = new dci_alloc_struct;
        alloc->rnti         = rnti;
        alloc->ci_presence  = carrier_ind;
        alloc->rv_idx       = rv_idx;
        alloc->N_prb        = alloc_len;
        alloc->N_layers     = N_layers;
        alloc->tx_mode      = tx_mode;
        alloc->N_dl_harq    = N_dl_harq;
        alloc->mcs          = mcs;
        alloc->ndi          = ndi;
        alloc->n_antenna    = N_ant;
        alloc->N_dl_prb     = N_dl_prb;
        alloc->prb[0][0]    = RB_start;
        // FIXME: tcp command isn't supported
        alloc->tpc          = 0;
    }

    dci_gen_impl::~dci_gen_impl()
    {
        delete alloc;
    }

    int
    dci_gen_impl::work(int noutput_items,
			  gr_vector_const_void_star &input_items,
			  gr_vector_void_star &output_items)
    {
        uint8 *out = (uint8 *) output_items[0];

        uint32 N_out_bits = 0;
        usleep(20000);

        if(!dci_type.compare("1A"))
        {
            dci_1a_pack(alloc,
                        alloc->ci_presence,
                        alloc->N_dl_prb,
                        alloc->n_antenna,
                        out,
                        &N_out_bits);

            // adding tag ...
            add_item_tag(0,
                         nitems_written(0),
                         pmt::string_to_symbol("length"),
                         pmt::from_long(N_out_bits));

//            cout << "DCI generator" << endl;

        }
        else
        {
            throw runtime_error("DCI type isn't recognized");
        }

        // Tell runtime system how many output items we produced.
        return N_out_bits;
    }

    void dci_gen_impl::dci_1a_pack(dci_alloc_struct *alloc,
                                   bool ci_presence,
                                   uint32                           N_rb_dl,
                                   uint8                            N_ant,
                                   uint8                           *out_bits,
                                   uint32                          *N_out_bits)
    {
        uint32  RIV;
        uint32  RIV_length;
        uint32  size;
        uint32  N_prb_1a;
        uint8  *dci = out_bits;

        // Carrier indicator
        if(ci_presence)
        {
            printf("WARNING: Not handling carrier indicator (instead replaced zeros)\n");
            phy_value_2_bits(0, &dci, 3);
        }

        // Format 0/1A flag is set to format 1A
        phy_value_2_bits(1, &dci, 1);

        if(LIBLTE_MAC_SI_RNTI        == alloc->rnti ||
                LIBLTE_MAC_P_RNTI         == alloc->rnti ||
                (LIBLTE_MAC_RA_RNTI_START <= alloc->rnti &&
                 LIBLTE_MAC_RA_RNTI_END   >= alloc->rnti))
        {
            // FIXME: Only supporting localized VRBs
            phy_value_2_bits(DCI_VRB_TYPE_LOCALIZED, &dci, 1);
            RIV_length = (uint32)ceilf(logf(N_rb_dl*(N_rb_dl+1)/2)/logf(2));
            if((alloc->N_prb-1) <= (N_rb_dl/2))
            {
                RIV = N_rb_dl*(alloc->N_prb-1) +  0;  //alloc->prb[0][0];
            }else{
                RIV = N_rb_dl*(N_rb_dl-alloc->N_prb+1) + (N_rb_dl - 1 - 0);    //alloc->prb[0][0]);
            }
            phy_value_2_bits(RIV, &dci, RIV_length);

            // Modulation and coding scheme
            phy_value_2_bits(alloc->mcs, &dci, 5);

            // HARQ process number, FIXME: FDD only
            phy_value_2_bits(alloc->N_dl_harq, &dci, 3);

            // New data indicator
            phy_value_2_bits(0, &dci, 1);

            // Redundancy version
            phy_value_2_bits(alloc->rv_idx, &dci, 2);

            // TPC
            N_prb_1a = 3;
            phy_value_2_bits(1, &dci, 2);

            // Calculate the TBS
            alloc->tbs = TBS_71721[alloc->mcs][N_prb_1a-1];
        }else{
            // FIXME: Only supporting localized VRBs
            phy_value_2_bits(DCI_VRB_TYPE_LOCALIZED, &dci, 1);
            RIV_length = (uint32)ceilf(logf(N_rb_dl*(N_rb_dl+1)/2)/logf(2));
            if((alloc->N_prb-1) <= (N_rb_dl/2))
            {
                RIV = N_rb_dl*(alloc->N_prb-1) + alloc->prb[0][0];
            }else{
                RIV = N_rb_dl*(N_rb_dl - alloc->N_prb + 1) + (N_rb_dl - 1 - alloc->prb[0][0]);        //alloc->prb[0][0]);
            }
            phy_value_2_bits(RIV, &dci, RIV_length);

            // Modulation and coding scheme
            phy_value_2_bits(alloc->mcs, &dci, 5);

            // HARQ process number, FIXME: FDD only
            phy_value_2_bits(alloc->N_dl_harq, &dci, 3);

            // New data indicator
            phy_value_2_bits(alloc->ndi, &dci, 1);

            // Redundancy version
            phy_value_2_bits(alloc->rv_idx, &dci, 2);

            // TPC
            phy_value_2_bits(alloc->tpc, &dci, 2);

            // Calculate the TBS
            alloc->tbs = TBS_71721[alloc->mcs][alloc->N_prb-1];
        }

        // Pad if needed
        size = dci - out_bits;
        if(size == 12 ||
           size == 14 ||
           size == 16 ||
           size == 20 ||
           size == 24 ||
           size == 26 ||
           size == 32 ||
           size == 40 ||
           size == 44 ||
           size == 56)
        {
            size++;
            phy_value_2_bits(0, &dci, 1);
        }
        *N_out_bits = size;
    }

    void dci_gen_impl::phy_value_2_bits(uint32   value,
                                   uint8  **bits,
                                   uint32   N_bits)
    {
        uint32 i;

        for(i=0; i<N_bits; i++)
        {
            (*bits)[i] = (value >> (N_bits-i-1)) & 0x1;
        }
        *bits += N_bits;
    }


